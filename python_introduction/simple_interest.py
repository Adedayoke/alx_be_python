# simple_interest.py
# Simple Interest Calculator
# This program calculates simple interest using the formula: I = P * R * T

# Define the required variables with specified values
principal = 1000    # Principal amount ($1000)
rate = 0.05         # Annual interest rate (5% as decimal)
time = 3            # Time period in years

# Calculate simple interest using the formula I = P * R * T
interest = principal * rate * time

# Print the calculated interest in the required format
print(f"The simple interest is: {interest}")